package sourceStudy.springSourceStudy.com.model;

public class SimpleBean {
    private String connctionString;
    private String username;
    private String password;

    public String getConnctionString() {
        return connctionString;
    }

    public void setConnctionString(String connctionString) {
        this.connctionString = connctionString;
    }

    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getPassword() {
        return password;
    }

    public void setPassword(String password) {
        this.password = password;
    }
}
